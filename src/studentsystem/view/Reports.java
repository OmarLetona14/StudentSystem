    public Reports() {
        initComponents();
        this.setLocationRelativeTo(null);
        for(int i=0; i<GradeTableModel.gradesList.listSize();i++){
            Grade currentGrade = null;
            try {
                currentGrade = GradeTableModel.gradesList.getGradeAt(i);
            } catch (Exception ex) {
                Logger.getLogger(GradeAdministrationWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(currentGrade!=null){
                cbCursoReporte1.addItem(currentGrade.getName());
                cbCursoReporte4.addItem(currentGrade.getName());
            }
            
        }
        for(int i=0; i<StudentTableModel.studentsList.listSize();i++){
            Student currentStudent = null;
            try {
                currentStudent = StudentTableModel.studentsList.getStudentAt(i);
            } catch (Exception ex) {
                Logger.getLogger(GradeAdministrationWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(currentStudent!=null){
                cbEstudianteReporte2.addItem(currentStudent.getName());
                cbEstudianteReporte3.addItem(currentStudent.getName());
                cbEstudianteReporte9.addItem(currentStudent.getName());
                cbEstudianteReporte10.addItem(currentStudent.getName());
            }   
        }
        for(int i=0; i<ProfessorTableModel.professorsList.listSize();i++){
            Professor currentProfessor = null;
            try {
                currentProfessor = ProfessorTableModel.professorsList.getProfessorAt(i);
            } catch (Exception ex) {
                Logger.getLogger(GradeAdministrationWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(currentProfessor!=null){
                cbCatedraticoReporte8.addItem(currentProfessor.getName());
            }   
        }
        for(int i=0; i<LoginWindow.listaSemestres.listSize();i++){
            Semester currentSemester = null;
            try {
                currentSemester = LoginWindow.listaSemestres.getSemesterAt(i);
            } catch (Exception ex) {
                Logger.getLogger(GradeAdministrationWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(currentSemester!=null){
                cbSemestreReporte2.addItem(currentSemester.getSemesterName());
                cbSemestreReporte4.addItem(currentSemester.getSemesterName());
                cbSemestreReporte5.addItem(currentSemester.getSemesterName());
                cbSemestreReporte7.addItem(currentSemester.getSemesterName());
                cbSemestreReporte9.addItem(currentSemester.getSemesterName());
            }   
        }
    }

