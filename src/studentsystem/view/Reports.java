    public int idSemester(JComboBox cbCombo){
        Semester currentSemester = LoginWindow.listaSemestres.getSemesterByName(String.valueOf(cbCombo.getSelectedItem()));
        return currentSemester.getIdSemester();
    }
    
    public int idGrade(JComboBox cbCombo){
        if(!cbCombo.getSelectedItem().equals("")){
            Grade currentG = GradeTableModel.gradesList.getGradeByName(String.valueOf(cbCombo.getSelectedItem()));
            return currentG.getIdGrade();
        }else{
            return 0;
        }
    }
    public int idProfessor(JComboBox cbCombo){
        if(!cbCombo.getSelectedItem().equals("")){
            Professor currentS = ProfessorTableModel.professorsList.getProfessorByName(String.valueOf(cbCombo.getSelectedItem()));
            return currentS.getIdProfessor();
        }else{
            return 0;
        }
    }
    
    public int idStudent(JComboBox cbCombo){
        if(!cbCombo.getSelectedItem().equals("")){
            Student currentS = StudentTableModel.studentsList.getStudentByName(String.valueOf(cbCombo.getSelectedItem()));
            return currentS.getIdStudent();
        }else{
            return 0;
        }
    }
    
